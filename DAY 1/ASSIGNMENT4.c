//Assignment 4: Write a program to print multiplication table of the number given by user without using loop.
#include<stdio.h>
int main()
{
	float f;
	printf("Enter number: ");
	scanf("%f",&f);
	printf("%f*1=%f\n",f,f*1);
	printf("%f*2=%f\n",f,f*2);
	printf("%f*3=%f\n",f,f*3);
	printf("%f*4=%f\n",f,f*4);
	printf("%f*5=%f\n",f,f*5);
	printf("%f*6=%f\n",f,f*6);
	printf("%f*7=%f\n",f,f*7);	
	printf("%f*8=%f\n",f,f*8);
	printf("%f*9=%f\n",f,f*9);
	printf("%f*10=%f\n",f,f*10);
}